CREATE TABLE IF NOT EXISTS milestones (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    milestone_status TEXT,
    ntp_requested_date DATE,
    ntp_received_date DATE,
    pace_ntp_req_date DATE,
    pace_ntp_rec_date DATE,
    hdm_ntp_req_date DATE,
    hdm_ntp_rec_date DATE,
    m1_requested_date DATE,
    m1_approval_date DATE,
    m2_requested_date DATE,
    m2_approval_date DATE,
    m1_5_requested_date DATE,
    m1_5_approval_date DATE,
    m3_requested_date DATE,
    m3_approval_date DATE,
    milestone_notes TEXT,
    perm_to_apply_date DATE,
    perm_to_apply_rec DATE
);